                        // Forward pass
                        double[] trainOutputEmbedding = rnn.Forward(inputEmbedding);

                        // Calculate Mean Squared Error loss for the current word pair
                        double loss = 0;
                        for (int j = 0; j < trainOutputEmbedding.Length; j++)
                        {
